def _daily_distribution(rows: list[dict[str, Any]]) -> list[dict[str, Any]]:
    groups: dict[str, list[dict[str, Any]]] = {}
    for row in rows:
        groups.setdefault(row["date"], []).append(row)

    output: list[dict[str, Any]] = []
    for date, group in groups.items():
        room_rows = [row for row in group if row["source"] == "room"]
        live_rows = [row for row in group if row["source"] == "live"]
        output.append({
            "date": date,
            "total_score": _sum_score(group),
            "room_score": _sum_score(room_rows),
            "live_score": _sum_score(live_rows),
            "total_count": sum(row["gift_count"] for row in group),
            "records": len(group),
            "room_records": len(room_rows),
            "live_records": len(live_rows),
            "senders": len({_sender_key(row) for row in group}),
            "gift_kinds": len({_gift_key(row) for row in group}),
            "live_sessions": len({row["live_id"] for row in live_rows if row["live_id"]}),
            "first_event_time": min((row["event_time"] for row in group if row["event_time"]), default=""),
            "latest_event_time": max((row["event_time"] for row in group if row["event_time"]), default=""),
        })
    output.sort(key=lambda row: str(row["date"]), reverse=True)
    return output


